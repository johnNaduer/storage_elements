mi_lista = [1, 2, 3, 4, 5]
print(mi_lista)
# Output: [1, 2, 3, 4, 5]

mi_tupla = (1, 2, 3, 4, 5)
print(mi_tupla)
# Output: (1, 2, 3, 4, 5)

mi_diccionario = {'nombre': 'Juan', 'edad': 30, 'profesion': 'Desarrollador'}
print(mi_diccionario)
# Output: {'nombre': 'Juan', 'edad': 30, 'profesion': 'Desarrollador'}

mi_conjunto = {1, 2, 3, 4, 5}
print(mi_conjunto)
# Output: {1, 2, 3, 4, 5}


# Example of duplicates in a dictionary
dict_with_duplicates = {'apple': 1, 'banana': 2, 'apple': 3}
print(dict_with_duplicates) # Output: {'apple': 3, 'banana': 2}

# Example of duplicates in a set
set_with_duplicates = {1, 2, 3, 2, 1}
print(set_with_duplicates) # Output: {1, 2, 3}
