
# Python code to demonstrate Type conversion
# using int(), float()
  
# initializing string
s = "10010"
  
# printing string converting to int base 2
c = int(s,2)
print ("After converting to integer base 2 : ", end="")
print (c)
  
t = "11001"
m = int(t)
print ("After converting to integer base 2 : ", end="")
print (m)

# printing string converting to float
e = float(s)
print ("After converting to float : ", end="")
print (e)
