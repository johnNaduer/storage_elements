import datetime

# Obtener la fecha y hora actual
now = datetime.datetime.now()

# Imprimir la fecha y hora actual
print("Fecha y hora actual:", now)

# Obtener el año actual
year = now.year
print("Año actual:", year)

# Obtener el mes actual
month = now.month
print("Mes actual:", month)

# Obtener el día actual
day = now.day
print("Día actual:", day)

# Obtener la hora actual
hour = now.hour
print("Hora actual:", hour)

# Obtener el minuto actual
minute = now.minute
print("Minuto actual:", minute)

# Obtener el segundo actual
second = now.second
print("Segundo actual:", second)
